'use client';

import { is } from 'date-fns/locale';

export enum PinIcons {
  mapPin = 'mapPin',
  mortarBoard = 'mortarBoard',
  arrowLeftCircle = 'arrowLeftCircle'
}

function getPinIcon(pinIcon: PinIcons, isFilled: boolean) {
  switch (pinIcon) {
    case PinIcons.mapPin:
      return getPinSVG(isFilled);
    case PinIcons.mortarBoard:
      return getMortarBoard(isFilled);
    case PinIcons.arrowLeftCircle:
      return getArrowLeftCircleSVG(isFilled);
  }
}

export const PinButton = ({
  pinIcon,
  isPinned,
  setPinned,
  classNames
}: {
  pinIcon: PinIcons;
  classNames: string;
  isPinned: boolean;
  setPinned: Function;
}) => {
  return (
    <>
      <label
        className={`${classNames} swap stroke-current hover:stroke-accent hover:fill-accent fill-current`}
      >
        <input
          type="checkbox"
          className={`cursor-pointer z-20`}
          checked={isPinned}
          onChange={() => setPinned()}
        />
        {getPinIcon(pinIcon, false)}

        {getPinIcon(pinIcon, true)}
      </label>
    </>
  );
};

